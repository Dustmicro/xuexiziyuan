
//public表示的是公开的
//class表示定义一个类
//HelloWorld表示一个类名
public class HelloWorld{//表示定义一个公开的类，该类名叫做HelloWorld
	//类体中不允许直接编写java语句（除声明变量之外）
	//类体
	/*  
	public表示公开的
	static表示静态的
	void 表示空
	main表示方法名是main
	(String[] args) 是一个main方法的形式参数列表
	*/
	public static void main(String[] args){//表示定义一个公开的静态的主方法，程序的入口，固定方式
		//方法体
		//java 语句以";"终止
		System.out.println("罗明俪是憨der!!!!");
		//向控制台输出消息
		System.out.println("对对对没错");
	}
}